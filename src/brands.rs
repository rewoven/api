use serde::{Deserialize, Serialize};

#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct BrandRating {
    pub name: String,
    pub slug: String,
    pub overall_score: u8,
    pub grade: String,
    pub environmental_score: u8,
    pub labor_score: u8,
    pub transparency_score: u8,
    pub animal_welfare_score: u8,
    pub price_range: String,
    pub country: String,
    pub category: String,
    pub certifications: Vec<String>,
    pub summary: String,
    pub website: String,
}

fn brand(
    name: &str,
    overall_score: u8,
    environmental_score: u8,
    labor_score: u8,
    transparency_score: u8,
    animal_welfare_score: u8,
    price_range: &str,
    country: &str,
    category: &str,
    certifications: &[&str],
    summary: &str,
    website: &str,
) -> BrandRating {
    let grade = match overall_score {
        90..=100 => "A+",
        80..=89 => "A",
        70..=79 => "B",
        60..=69 => "C",
        50..=59 => "C-",
        40..=49 => "D",
        30..=39 => "D-",
        20..=29 => "E",
        10..=19 => "F",
        _ => "F-",
    };
    BrandRating {
        name: name.to_string(),
        slug: name
            .to_lowercase()
            .replace(' ', "-")
            .replace('\'', "")
            .replace('&', "and")
            .replace('.', "")
            .chars()
            .filter(|c| c.is_alphanumeric() || *c == '-')
            .collect(),
        overall_score,
        grade: grade.to_string(),
        environmental_score,
        labor_score,
        transparency_score,
        animal_welfare_score,
        price_range: price_range.to_string(),
        country: country.to_string(),
        category: category.to_string(),
        certifications: certifications.iter().map(|s| s.to_string()).collect(),
        summary: summary.to_string(),
        website: website.to_string(),
    }
}

